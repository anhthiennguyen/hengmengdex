const STOPS = [50, 100, 200, 300, 400, 500, 600, 700, 800, 900];
// Mirrors the lightness curve of the original static `royal` Tailwind
// palette (see tailwind.config.js) so dynamically-generated ramps read the
// same way — hue/saturation come from the dex's chosen color, lightness is
// this fixed curve.
const LIGHTNESS = [95, 90, 80, 68, 58, 50, 42, 35, 28, 22];

function hexToHsl(hex) {
  const r = parseInt(hex.slice(1, 3), 16) / 255;
  const g = parseInt(hex.slice(3, 5), 16) / 255;
  const b = parseInt(hex.slice(5, 7), 16) / 255;

  const max = Math.max(r, g, b);
  const min = Math.min(r, g, b);
  const delta = max - min;

  let h = 0;
  if (delta !== 0) {
    if (max === r) h = ((g - b) / delta) % 6;
    else if (max === g) h = (b - r) / delta + 2;
    else h = (r - g) / delta + 4;
    h *= 60;
    if (h < 0) h += 360;
  }

  const l = (max + min) / 2;
  const s = delta === 0 ? 0 : delta / (1 - Math.abs(2 * l - 1));

  return { h, s: s * 100, l: l * 100 };
}

function hslToHex(h, s, l) {
  s /= 100;
  l /= 100;
  const c = (1 - Math.abs(2 * l - 1)) * s;
  const x = c * (1 - Math.abs(((h / 60) % 2) - 1));
  const m = l - c / 2;

  let [r, g, b] = [0, 0, 0];
  if (h < 60) [r, g, b] = [c, x, 0];
  else if (h < 120) [r, g, b] = [x, c, 0];
  else if (h < 180) [r, g, b] = [0, c, x];
  else if (h < 240) [r, g, b] = [0, x, c];
  else if (h < 300) [r, g, b] = [x, 0, c];
  else [r, g, b] = [c, 0, x];

  const toHex = (v) =>
    Math.round((v + m) * 255)
      .toString(16)
      .padStart(2, '0');

  return `#${toHex(r)}${toHex(g)}${toHex(b)}`;
}

export function buildAccentRamp(baseHex) {
  const { h } = hexToHsl(baseHex);
  const s = Math.min(90, Math.max(35, hexToHsl(baseHex).s));

  const ramp = {};
  STOPS.forEach((stop, i) => {
    ramp[stop] = hslToHex(h, s, LIGHTNESS[i]);
  });
  return ramp;
}
